/**
 * Design to CSS Converter
 * Converts component design properties from the design panel into clean, functional CSS
 * This ensures the exported code matches the visual design created in the builder
 */

import type { ComponentData } from "../App"

interface CSSRule {
  selector: string
  properties: Record<string, string>
}

// Convert design properties to valid CSS
const sanitizeValue = (value: any): string => {
  if (value === null || value === undefined || value === "") return ""

  // Handle objects (skip them)
  if (typeof value === "object") return ""

  // Handle numbers that need units
  if (typeof value === "number") {
    // If it looks like a position or size, add px
    if (value > 0 && value < 10000) return `${value}px`
    return String(value)
  }

  const strValue = String(value).trim()

  // Already has units or is a keyword
  if (
    strValue.includes("px") ||
    strValue.includes("%") ||
    strValue.includes("rem") ||
    strValue.includes("em") ||
    strValue.includes("rgba") ||
    strValue.includes("rgb") ||
    strValue.includes("linear-gradient") ||
    strValue.includes("radial-gradient") ||
    strValue.includes("transparent") ||
    strValue.includes("inherit") ||
    strValue.includes("auto") ||
    strValue.includes("none")
  ) {
    return strValue
  }

  return strValue
}

const toKebabCase = (str: string): string => {
  return str
    .replace(/([a-z])([A-Z])/g, "$1-$2")
    .replace(/([A-Z])([A-Z][a-z])/g, "$1-$2")
    .toLowerCase()
}

// Convert component styles to CSS rules
export const componentToCSSRule = (component: ComponentData): CSSRule | null => {
  if (!component.style || Object.keys(component.style).length === 0) {
    return null
  }

  const className = component.props?.className || `component-${component.type}-${component.id}`
  const properties: Record<string, string> = {}

  Object.entries(component.style).forEach(([key, value]) => {
    const cssKey = toKebabCase(key)
    const cssValue = sanitizeValue(value)

    if (cssValue && cssValue.length > 0) {
      properties[cssKey] = cssValue
    }
  })

  if (Object.keys(properties).length === 0) {
    return null
  }

  return {
    selector: `.${className}`,
    properties,
  }
}

// Format CSS rule to string
const formatCSSRule = (rule: CSSRule): string => {
  const props = Object.entries(rule.properties)
    .map(([key, value]) => `  ${key}: ${value};`)
    .join("\n")

  return `${rule.selector} {\n${props}\n}`
}

// Generate complete component-specific CSS
export const generateComponentSpecificCSS = (components: ComponentData[]): string => {
  const rules: CSSRule[] = []

  components.forEach((component) => {
    const rule = componentToCSSRule(component)
    if (rule) {
      rules.push(rule)
    }
  })

  if (rules.length === 0) {
    return ""
  }

  return `/* ============================================
   Component-Specific Styles (Auto-generated from Design Panel)
   ============================================ */

${rules.map(formatCSSRule).join("\n\n")}`
}

